public class App {
    public static void main(String[] args) throws Exception {
        // Declarando e inicializando um array de inteiros
        int[] numeros = new int[5]; // Cria um array com tamanho 5
        // Atribuindo valores aos elementos do array
        numeros[0] = 10;
        numeros[1] = 20;
        numeros[2] = 30;
        numeros[3] = 40;
        numeros[4] = 50;

        // Acessando elementos do array
        System.out.println("Primeiro número: " + numeros[0]);
        System.out.println("Terceiro número: " + numeros[2]);
    }
}
